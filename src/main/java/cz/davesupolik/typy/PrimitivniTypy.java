package cz.davesupolik.typy;

public class PrimitivniTypy {
    public static void run() {
        // Primitivní datové typy
        int celeCislo = 42;        // Celé číslo
        double desetinneCislo = 3.14; // Desetinné číslo
        char znak = 'A';           // Jeden znak
        boolean pravdaNeboNepravda = true; // Logická hodnota

        // Výpis na konzoli
        System.out.println("Celé číslo: " + celeCislo);
        System.out.println("Desetinné číslo: " + desetinneCislo);
        System.out.println("Znak: " + znak);
        System.out.println("Pravda nebo nepravda: " + pravdaNeboNepravda);
    }
}