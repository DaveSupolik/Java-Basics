package cz.davesupolik.promenne;

public class Example1 {
    public static void run() {
        // OPERACE S PROMĚNNÝMI
        // Deklarace a inicializace proměnných
        int vek = 25; // Celé číslo
        double teplota = 36.6; // Desetinné číslo
        String jmeno = "Petr"; // Textový řetězec
        boolean jePrihlasen = true; // Pravda/nepravda

        // Výstup hodnot na konzoli
        System.out.println("Jméno: " + jmeno);
        System.out.println("Věk: " + vek);
        System.out.println("Teplota: " + teplota + "°C");
        System.out.println("Je přihlášen: " + jePrihlasen);
    }
}